//! Configuration object accepted by the `tunacode` MCP tool-call.

use mcp_types::Tool;
use mcp_types::ToolInputSchema;
use schemars::JsonSchema;
use schemars::r#gen::SchemaSettings;
use serde::Deserialize;
use serde::Serialize;
use std::collections::HashMap;
use std::path::PathBuf;
use tunacode_core::protocol::AskForApproval;
use tunacode_protocol::config_types::SandboxMode;
use tunacode_utils_json_to_toml::json_to_toml;

/// Client-supplied configuration for a `tunacode` tool-call.
#[derive(Debug, Clone, Serialize, Deserialize, JsonSchema, Default)]
#[serde(rename_all = "kebab-case")]
pub struct TunacodeToolCallParam {
    /// The *initial user prompt* to start the tunacode conversation.
    pub prompt: String,

    /// Optional override for the model name (e.g. "o3", "o4-mini").
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub model: Option<String>,

    /// Configuration profile from config.toml to specify default options.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub profile: Option<String>,

    /// Working directory for the session. If relative, it is resolved against
    /// the server process's current working directory.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub cwd: Option<String>,

    /// Approval policy for shell commands generated by the model:
    /// `untrusted`, `on-failure`, `on-request`, `never`.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub approval_policy: Option<TunacodeToolCallApprovalPolicy>,

    /// Sandbox mode: `read-only`, `workspace-write`, or `danger-full-access`.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub sandbox: Option<TunacodeToolCallSandboxMode>,

    /// Individual config settings that will override what is in
    /// tunacode_HOME/config.toml.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub config: Option<HashMap<String, serde_json::Value>>,

    /// The set of instructions to use instead of the default ones.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub base_instructions: Option<String>,

    /// Whether to include the plan tool in the conversation.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub include_plan_tool: Option<bool>,
}

/// Custom enum mirroring [`AskForApproval`], but has an extra dependency on
/// [`JsonSchema`].
#[derive(Debug, Clone, Serialize, Deserialize, JsonSchema, PartialEq)]
#[serde(rename_all = "kebab-case")]
pub enum TunacodeToolCallApprovalPolicy {
    Untrusted,
    OnFailure,
    OnRequest,
    Never,
}

impl From<TunacodeToolCallApprovalPolicy> for AskForApproval {
    fn from(value: TunacodeToolCallApprovalPolicy) -> Self {
        match value {
            TunacodeToolCallApprovalPolicy::Untrusted => AskForApproval::UnlessTrusted,
            TunacodeToolCallApprovalPolicy::OnFailure => AskForApproval::OnFailure,
            TunacodeToolCallApprovalPolicy::OnRequest => AskForApproval::OnRequest,
            TunacodeToolCallApprovalPolicy::Never => AskForApproval::Never,
        }
    }
}

/// Custom enum mirroring [`SandboxMode`] from config_types.rs, but with
/// `JsonSchema` support.
#[derive(Debug, Clone, Serialize, Deserialize, JsonSchema, PartialEq)]
#[serde(rename_all = "kebab-case")]
pub enum TunacodeToolCallSandboxMode {
    ReadOnly,
    WorkspaceWrite,
    DangerFullAccess,
}

impl From<TunacodeToolCallSandboxMode> for SandboxMode {
    fn from(value: TunacodeToolCallSandboxMode) -> Self {
        match value {
            TunacodeToolCallSandboxMode::ReadOnly => SandboxMode::ReadOnly,
            TunacodeToolCallSandboxMode::WorkspaceWrite => SandboxMode::WorkspaceWrite,
            TunacodeToolCallSandboxMode::DangerFullAccess => SandboxMode::DangerFullAccess,
        }
    }
}

/// Builds a `Tool` definition (JSON schema etc.) for the tunacode tool-call.
pub(crate) fn create_tool_for_tunacode_tool_call_param() -> Tool {
    let schema = SchemaSettings::draft2019_09()
        .with(|s| {
            s.inline_subschemas = true;
            s.option_add_null_type = false;
        })
        .into_generator()
        .into_root_schema_for::<TunacodeToolCallParam>();

    #[expect(clippy::expect_used)]
    let schema_value =
        serde_json::to_value(&schema).expect("tunacode tool schema should serialise to JSON");

    let tool_input_schema =
        serde_json::from_value::<ToolInputSchema>(schema_value).unwrap_or_else(|e| {
            panic!("failed to create Tool from schema: {e}");
        });

    Tool {
        name: "tunacode".to_string(),
        title: Some("tunacode".to_string()),
        input_schema: tool_input_schema,
        // TODO(mbolin): This should be defined.
        output_schema: None,
        description: Some(
            "Run a tunacode session. Accepts configuration parameters matching the tunacode Config struct.".to_string(),
        ),
        annotations: None,
    }
}

impl TunacodeToolCallParam {
    /// Returns the initial user prompt to start the tunacode conversation and the
    /// effective Config object generated from the supplied parameters.
    pub fn into_config(
        self,
        tunacode_linux_sandbox_exe: Option<PathBuf>,
    ) -> std::io::Result<(String, tunacode_core::config::Config)> {
        let Self {
            prompt,
            model,
            profile,
            cwd,
            approval_policy,
            sandbox,
            config: cli_overrides,
            base_instructions,
            include_plan_tool,
        } = self;

        // Build the `ConfigOverrides` recognized by tunacode-core.
        let overrides = tunacode_core::config::ConfigOverrides {
            model,
            review_model: None,
            config_profile: profile,
            cwd: cwd.map(PathBuf::from),
            approval_policy: approval_policy.map(Into::into),
            sandbox_mode: sandbox.map(Into::into),
            model_provider: None,
            tunacode_linux_sandbox_exe,
            base_instructions,
            include_plan_tool,
            include_apply_patch_tool: None,
            include_view_image_tool: None,
            show_raw_agent_reasoning: None,
            tools_web_search_request: None,
        };

        let cli_overrides = cli_overrides
            .unwrap_or_default()
            .into_iter()
            .map(|(k, v)| (k, json_to_toml(v)))
            .collect();

        let cfg = tunacode_core::config::Config::load_with_cli_overrides(cli_overrides, overrides)?;

        Ok((prompt, cfg))
    }
}

#[derive(Debug, Clone, Serialize, Deserialize, JsonSchema)]
#[serde(rename_all = "camelCase")]
pub struct TunacodeToolCallReplyParam {
    /// The conversation id for this tunacode session.
    pub conversation_id: String,

    /// The *next user prompt* to continue the tunacode conversation.
    pub prompt: String,
}

